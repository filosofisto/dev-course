package lista04.itemsPersonagem;

public class Posicao {

	private int pontoX;
	private int pontoy;
	
	public Posicao(int pontoX, int pontoY) {
		this.pontoX = pontoX;
		this.pontoy = pontoY;
	}

	public int getPontoX() {
		return pontoX;
	}

	public void setPontoX(int pontoX) {
		this.pontoX = pontoX;
	}

	public int getPontoy() {
		return pontoy;
	}

	public void setPontoy(int pontoy) {
		this.pontoy = pontoy;
	}

}
